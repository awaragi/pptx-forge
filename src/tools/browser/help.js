function icon(name) {
  return `<span class="icon icon-${name} help-icon"></span>`;
}

function chip(glyph) {
  return `<span class="help-chip">${glyph}</span>`;
}

function item(glyph, label, description) {
  return `<li>${chip(glyph)}<span><strong>${label}</strong> — ${description}</span></li>`;
}

