const el = {
  btn: document.getElementById('help-btn'),
  overlay: document.getElementById('help-overlay'),
  closeBtn: document.getElementById('help-overlay-close'),
  emoji: document.getElementById('help-emoji'),
  title: document.getElementById('help-title'),
  body: document.getElementById('help-body'),
  prevBtn: document.getElementById('help-prev-btn'),
  nextBtn: document.getElementById('help-next-btn'),
  dots: document.getElementById('help-dots'),
};

let index = 0;

function renderScreen() {
  const screen = SCREENS[index];
  el.emoji.textContent = screen.emoji;
  el.title.textContent = screen.title;
  el.body.innerHTML = screen.body;
  el.prevBtn.disabled = index === 0;
  el.nextBtn.disabled = index === SCREENS.length - 1;
  el.dots.querySelectorAll('.help-dot').forEach((dot, i) => {
    dot.classList.toggle('active', i === index);
    dot.setAttribute('aria-current', i === index ? 'true' : 'false');
  });
}

function renderDots() {
  el.dots.innerHTML = '';
  SCREENS.forEach((screen, i) => {
    const dot = document.createElement('button');
    dot.type = 'button';
    dot.className = 'help-dot';
    dot.setAttribute('aria-label', `Go to screen ${i + 1}: ${screen.title}`);
    dot.addEventListener('click', () => goTo(i));
    el.dots.appendChild(dot);
  });
}

function goTo(i) {
  index = Math.max(0, Math.min(SCREENS.length - 1, i));
  renderScreen();
}

// Reopening always starts at screen 1 — each open is "start of the tour",
// not a resume of wherever the user last left off.
export function openHelp() {
  index = 0;
  renderScreen();
  el.overlay.classList.add('visible');
}

export function closeHelp() {
  el.overlay.classList.remove('visible');
}

export function isHelpOpen() {
  return el.overlay.classList.contains('visible');
}

export function nextHelpScreen() {
  goTo(index + 1);
}

export function prevHelpScreen() {
  goTo(index - 1);
}

// isFirstVisit must be computed by the caller before the default-workspace
// auto-create logic runs (see storage.js hasAnyWorkspaceData); this only
// decides whether to act on it, and records that the modal has been shown.
export function maybeAutoOpenHelp(isFirstVisit) {
  if (isFirstVisit && !hasSeenHelp()) {
    openHelp();
    markHelpSeen();
  }
}

renderDots();
el.btn.addEventListener('click', openHelp);
el.closeBtn.addEventListener('click', closeHelp);
el.overlay.addEventListener('click', (e) => {
  if (e.target === el.overlay) closeHelp();
});
el.prevBtn.addEventListener('click', prevHelpScreen);
el.nextBtn.addEventListener('click', nextHelpScreen);
